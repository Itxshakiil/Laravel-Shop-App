     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $attributes = array('razorpay_signature' => $request->razorpay_signature, 'razorpay_payment_id' => $request->razorpay_payment_id, 'razorpay_order_id' => $request->razorpay_order_id,);
        // returndd(RazorpayApi::connect()->utility->verifyPaymentSignature($attributes));
        if (RazorpayApi::connect()->utility->verifyPaymentSignature($attributes)) {
            $payment = $request->razorpay_payment_id;
            return view('payment.success', compact('payment'));
        }
    }
